import random
from scrapy.contrib.downloadermiddleware.useragent import UserAgentMiddleware


class RotateUserAgent(UserAgentMiddleware):
    def __init__(self, user_agent=''):
        self.user_agent = user_agent

    def process_request(self, request, spider):
        # 这句话用于随机选择user-agent
        ua = random.choice(self.user_agent_list)
        if ua:
            request.headers.setdefault('user-agent', ua)
        request.headers.setdefault('accept',
                                   "text/html,application/xhtml+xml,application/xml;q=0.9,image/webp,image/apng,*/*;q=0.8")
        request.headers.setdefault('accept-encoding', "gzip, deflate, br")
        request.headers.setdefault('accept-language', "zh-CN,zh;q=0.9")
        request.headers.setdefault('upgrade-insecure-requests', "1")
        request.headers.setdefault('method', "GET")

    # the default user_agent_list composes chrome,I E,firefox,Mozilla,opera,netscape
